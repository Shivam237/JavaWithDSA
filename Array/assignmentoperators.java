public class assignmentoperators {
    public static void main(String[] args) {
        int p =10;
        int q =5;
//        = operators
        q =p;
        System.out.println(q);

//        += operators
        p+=q;
        System.out.println(p);

//         -= operators
        p-=q;
        System.out.println(p);

//         /= operators
        p/=q;
        System.out.println(p);

//         %= operators
        p%=q;
        System.out.println(p);
    }
}
